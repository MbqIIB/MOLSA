create view DW_INVESTIGATIONVIEW as
SELECT -1 CASEID, 
       DW_GETDATETIME() LASTWRITTEN, 
       -1 RELATEDCASEID,
       -1 RELATEDCONCERNROLEID,
       -1  RESOLUTIONSTATUS,
       SUB.DWINVSUBTYPEKEY, 
       MILE.DWMILESTONEDURATIONKEY,
       -1 DWCCSOUTCOMEKEY,
       -1 DWMEETMILESTONEDURINDKEY    
FROM  DW_INVSUBTYPE SUB,
     DW_MILESTONEDURATION MILE,
     DW_CASE CAS,
     DW_CASESTATUSHISTORY CASEST,
     DW_CASESTATUS STA
WHERE   CAS.CASEID = -1
  AND CAS.DWCASEID = CASEST.DWCASEID
  AND CASEST.DWSTATUSID = STA.DWSTATUSID
  AND SUB.CODE ='-1'
  AND STA.STATUSNAME = 'Case Start'
  AND MILE.MILESTONENAME = 'Federal Investigation Duration'
  AND CASEST.FROMDATE BETWEEN MILE.STARTDATE AND COALESCE(MILE.ENDDATE,getDate());

