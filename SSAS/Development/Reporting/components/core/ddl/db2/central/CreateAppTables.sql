-- View: DW_DIMCASEOWNER
--==============================================================

create view DW_DIMCASEOWNER as
select
   co.caseownerid as ownername,
   max(co.DWCASEOWNERID) as DWCASEOWNERID,
   us.firstname as FirstName,
   us.lastname as LastName,
   co.CASEOWNERTYPEDESC as OwnerType,
   max(co.LASTWRITTEN) as LASTWRITTEN
from
   dw_caseowner co,
   dw_users us
where
   co.CASEOWNERID = us.CASEOWNERID
group by
   co.caseownerid,
   us.firstname,
   us.lastname,
   co.CASEOWNERTYPEDESC
union
select
   org.name as ownername,
   max(co.DWCASEOWNERID) as DWCASEOWNERID,
   NULLIF('','') as FirstName,
   NULLIF('','') as LastName,
   co.CASEOWNERTYPEDESC as OwnerType,
   max(co.LASTWRITTEN) as LASTWRITTEN
from
   dw_caseowner co,
   dw_orgunit org
where
   co.DWORGUNITID = org.DWORGUNITID
group by
   org.name,
   co.CASEOWNERTYPEDESC
union
select
   pos.name as ownername,
   max(co.DWCASEOWNERID) as DWCASEOWNERID,
   NULLIF('','') as FirstName,
   NULLIF('','') as LastName,
   co.CASEOWNERTYPEDESC as OwnerType,
   max(co.LASTWRITTEN) as LASTWRITTEN
from
   dw_caseowner co,
   dw_position pos
where
   co.DWPOSITIONID = pos.DWPOSITIONID
   and co.DWPOSITIONID <> -1
group by
   pos.name,
   co.CASEOWNERTYPEDESC
union
select
   wq.name as ownername,
   max(co.DWCASEOWNERID) as DWCASEOWNERID,
   NULLIF('','') as FirstName,
   NULLIF('','') as LastName,
   co.CASEOWNERTYPEDESC as OwnerType,
   max(co.LASTWRITTEN) as LASTWRITTEN
from
   dw_caseowner co,
   dw_workqueue wq
where
   co.DWWORKQUEUEID = wq.DWWORKQUEUEID
   and co.DWWORKQUEUEID <> -1
group by
   wq.name,
   co.CASEOWNERTYPEDESC;

